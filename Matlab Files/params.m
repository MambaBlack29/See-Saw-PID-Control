function [ params ] = params()

% pre-determined system parameters

params.m = 1; % mass of the ball (kg)
params.M = 5; % mass of the seesaw (kg)
params.L = 5; % length of each arm of the seesaw (m)
params.g = 9.81; % acceleration due to gravity (kg m/s^2)
params.r = 0.15; % radius of the ball (m)

end